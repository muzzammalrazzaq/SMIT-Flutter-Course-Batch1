void main(){
  //Pre-Increment Operator
  var preIncrement = 0;
  var preIncrement1 = ++preIncrement;
  print(preIncrement1);

  //Post-Increment Operator
  var postIncrement = 0;
  var postIncrement1 = postIncrement++;
  print(postIncrement);
  print(postIncrement1);

  //Pre-Decrement Operator
  var preDecrement = 0;
  var preDecrement1 = --preDecrement;
  print(preDecrement1);

  //Post-Decrement Operator
  var postDecrement = 0;
  var postDecrement1 = postDecrement--;
  print(postDecrement);
  print(postDecrement1);
}